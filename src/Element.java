/**
 * Représente les différents éléments auxquels peuvent être liés les attaques des Pokémon.
 *
 * Chaque attaque peut être liée à un élément, qui peut être l'un des éléments suivants :
 * Feu, Plante, Eau, Vol, Ténèbres, Normal, Poison, Glace, Électrik, Sol, Psy.
 *
 * Chaque valeur de l'énumération est simplement un nom d'élément.
 */
public enum Element {
    FEU,
    PLANTE,
    EAU,
    VOL,
    TENEBRES,
    NORMAL,
    POISON,
    GLACE,
    ELECTRIK,
    SOL,
    PSY
}
